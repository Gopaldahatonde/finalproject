        <h2>Duties of Authors</h2>
                <div class="section">
                    <h5>1. <u>Reporting Standards</u></h5>
                    <p>Authors of reports of original research should present an accurate account of the work performed
                        as well as an objective discussion of its significance. Underlying data should be represented
                        accurately in the paper. A paper should contain sufficient detail and references to permit
                        others to replicate the work. Fraudulent or knowingly inaccurate statements constitute unethical
                        behavior and are unacceptable.</p>
                </div>
            

        
                <div class="section">
                    <h5>2. <u>Data Access and Retention</u></h5>
                    <p>Authors are asked to provide the raw data in connection with a paper for editorial review, and
                        should be prepared to provide public access if practicable, and should in any event be prepared
                        to retain such data for a reasonable time after publication.</p>
                </div>
                
        

       
                <div class="section">
                    <h5>3. <u>Originality and Plagiarism</u></h5>
                    <p>The authors should ensure that they have written entirely original works, and if the authors have
                        used the work and/or words of others that this has been appropriately cited or quoted.</p>
                </div>
                

           
                <div class="section">
                    <h5>4. <u>Multiple, Redundant or Concurrent Publication</u></h5>
                    <p>An author should not in general publish manuscripts describing essentially the same research in
                        more than one journal or primary publication. Submitting the same manuscript to more than one
                        journal concurrently constitutes unethical publishingbehaviour and is unacceptable. Duplicate
                        publication is a publication that overlaps substantially with one already published, in press,
                        or in an electronic media submission. Redundant submission is the same manuscript that is
                        submitted to different journals at the same time. International copyright laws, ethical conduct,
                        and cost effective use of resources require that readers can be assured that what they are
                        reading is original. Submitted manuscripts should not have been published or currently submitted
                        elsewhere. Duplicate publication is a violation of the APA code of ethics by APA Publication
                        Manual, 2010 and will be grounds for prompt rejection of the submitted manuscript. If the editor
                        was not aware of the violation and the article has been published, a notice of duplicate
                        submission and the ethical violation will be published..</p>
                </div>
                

            
                <div class="section">
                    <h5>5. <u>Acknowledgement of Sources</u></h5>
                    <p>Proper acknowledgment of the work of others must always be given. Authors should cite
                        publications that have been influential in determining the nature of the reported work.</p>
                </div>
               

            
                <div class="section">
                    <h5>6. <u>Authorship of the Paper</u></h5>
                    <p>Authorship should be limited to those who have made a significant contribution to the conception,
                        design, execution, or interpretation of the reported study. All those who have made significant
                        contributions should be listed as co-authors. Where there are others who have participated in
                        certain substantive aspects of the research project, they should be acknowledged or listed as
                        contributors. The corresponding author should ensure that all appropriate co-authors and no
                        inappropriate co-authors are included on the paper, and that all co-authors have seen and
                        approved the final version of the paper and have agreed to its submission for publication..</p>
                </div>
                


           
                <div class="section">
                    <h5>7. <u>Disclosure and Conflicts of Interest</u></h5>
                    <p>All authors should disclose in their manuscript any financial or other substantive conflict of
                        interest that might be construed to influence the results or interpretation of their manuscript.
                        All sources of financial support for the project should be disclosed.</p>
                </div>
              

            
                <div class="section">
                    <h5>8. <u>Fundamental errors in published works</u></h5>
                    <p>When an author discovers a significant error or inaccuracy in his/her own published work, it is
                        the author's obligation to promptly notify the journal editor or publisher and cooperate with
                        the editor to retract or correct the paper.</p>
                </div>
                


